package com.fahime.java_1;

public class Loops2 {

	public static void main(String[] args) {
		//ForLoop();
        //WhileLoop();
        DoWhileLoop();
	}

    //For Loop
	public static void ForLoop(){
		
		for(int i = 1; i<=100; i++){
			System.out.println(i);
		}
	}
   //While Loop
	public static void WhileLoop(){
		int i = 1; 
		while(i<=100){
		System.out.println(i);
		i++;
	}
	}	
  //DoWhileLoop
	
   public static void DoWhileLoop(){
	   int i = 1;
	   do{
		  System.out.println(i);
		  i++;
	   }while(i<=100);
   }
	
		
		






}









